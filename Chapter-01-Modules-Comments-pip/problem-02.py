# Use the Python REPL to print the table of 5.
#Solution
# REPL stands for Read-Evaluate-Print Loop.
# It is an interactive Python shell where you can execute Python code line by line.
# You can use it like a calculator to perform mathematical calculations and test code quickly.
#Read---Python reads the code what you type
#Evaluate---It executes(evaluate) the code
#Print---It display the result.
# Loop → It waits for your next input and repeats the process.